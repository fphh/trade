data Row = Row {
  date :: !UTCTime
  , open :: !(Maybe Open)
  , high :: !(Maybe High)
  , low :: !(Maybe Low)
  , close :: !(Maybe Close)
  , volume :: !(Maybe Volume)
  , exDividend :: !Double
  , splitRatio :: !Double
  , adjOpen :: !Double
  , adjHigh :: !Double
  , adjLow :: !Double
  , adjClose :: !Double
  , adjVolume :: !Double
  } deriving (Show)


-- "Date","Open","High","Low","Close","Volume","Ex-Dividend","Split Ratio","Adj. Open","Adj. High","Adj. Low","Adj. Close","Adj. Volume"

instance FromNamedRecord Row where
  parseNamedRecord r =
    Row
    <$> (r .:: "Date")
    <*> (r .:: "Open")
    <*> (r .:: "High")
    <*> (r .:: "Low")
    <*> (r .:: "Close")
    <*> (r .:: "Volume")
    <*> (r .:: "Ex-Dividend")
    <*> (r .:: "Split Ratio")
    <*> (r .:: "Adj. Open")
    <*> (r .:: "Adj. High")
    <*> (r .:: "Adj. Low")
    <*> (r .:: "Adj. Close")
    <*> (r .:: "Adj. Volume")

instance ToRow WIKI where
  type RowTy WIKI = Row

instance RowInterface Row where
  dateR = date
  openR = open
  highR = high
  lowR = low
  closeR = close
  volumeR = volume

instance DateInterface Row where
  dateDI = date

